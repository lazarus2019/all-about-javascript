/*
    HTML DOM (Document Object Model) là 1 quy chuẩn của W3C (World Wide Web Consortium)
    => DOM được tạo ra khi trang web được tải và trình duyệt sẽ tạo ra mô hình tree DOM

    Cả website đều là mã html được trình duyệt parse và hệ thống hóa thành 1 mô hình được gọi là DOM
    => Mô hình này thể hiện tất cả các thành phần trên website

    Có 3 thành phần:
    1. Element: thành phần
    2. Attribute: thuộc tính
    3. Text: nội dung dạng chữ

    Javascript có thể thay đổi các thành phần trên => thay đổi cả website
    - Đọc dữ liệu và thay đổi các element
    - Thay đổi CSS style cho element
    - Thêm, xóa element hoặc thuộc tính của nó
    - Xử lý event cho các element

    Node: điểm giao nhau của sơ đồ DOM (có thể là 1 trong 3 thành phần trên)

    W3C DOM chia thành 3 phần:
    - Core DOM: Tiêu chuẩn cho tất cả tài liệu nói chung (Excel, PDF, Word,...)
    - XML DOM: Tiêu chuẩn cho tài liệu XML
    - HTML DOM: Tiêu chuẩn cho tài liệu HTML

    NOTE: HTML DOM không phải của Javascript, Javascript chỉ cung cấp bộ phương tiện để truy xuất vào HTML DOM
*/

